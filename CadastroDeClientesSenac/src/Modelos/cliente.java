String Nome;
String CPF;
String Telefone;
String Sexo;
String DataDeNascimento;
String Endereco;
    public int getId() {
        return Id;
    }

    public String getNome() {
        return Nome;
    }

    public String getCPF() {
        return CPF;
    }

